/**
 * Recursively widen literal string types to `string`.
 * This allows different locale dictionaries to share the same structural type
 * while preserving the key structure for autocompletion.
 */
type Widen<T> = T extends string
  ? string
  : T extends Record<string, unknown>
    ? { [K in keyof T]: Widen<T[K]> }
    : T

export type Dictionary = Widen<typeof ar>
